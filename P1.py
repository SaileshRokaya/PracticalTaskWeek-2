# Begin code

# Store input numbers
a = int(1)
b = int(2)
c = int(3)
d = int(4)
e = int(5)
f = int(6)
g = int(7)
h = int(8)

# Display output one per line
print ("Two to the power of", a, "is:", 2**a)
print ("Two to the power of", b, "is:", 2**b)
print ("Two to the power of", c, "is:", 2**c)
print ("Two to the power of", d, "is:", 2**d)
print ("Two to the power of", e, "is:", 2**e)
print ("Two to the power of", f, "is:", 2**f)
print ("Two to the power of", g, "is:", 2**g)
print ("Two to the power of", h, "is:", 2**h)
